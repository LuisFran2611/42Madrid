#include <unistd.h>

void	print(char n1[4], int i[2])
{
	n1[0] = i[0] / 10 + '0';
	n1[1] = i[0] % 10 + '0';
	n1[2] = i[1] / 10 + '0';
	n1[3] = i[1] % 10 + '0';
	write (1, &n1[0], 2);
	write (1, " ", 1);
	write (1, &n1[2], 2);
	if (!(i[0] == 98 && i[1] == 99))
	{
		write (1, ",", 1);
		write (1, " ", 1);
	}
}

void	ft_print_comb2(void)
{
	char	n1[4];
	int		i[2];

	i[0] = 0;
	i[1] = i[0] + 1;
	while (i[0] < 100)
	{
		while (i[1] < 100)
		{
			print(n1, i);
			i[1]++;
		}
		i[0]++;
		i[1] = i[0] + 1;
	}
}
